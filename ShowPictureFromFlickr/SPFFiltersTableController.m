#import "SPFFiltersTableController.h"

@interface SPFFiltersTableController ()
@property NSArray<CIFilter*> *filters;
@end

@implementation SPFFiltersTableController
- (instancetype) init{
    self = [super init];
    if (self){
        _filters = @[@"CIPhotoEffectNoir", @"CISepiaTone"];
    }
    return self;
    
}

- (void)viewDidLoad {
    [super viewDidLoad];
    
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
}

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return 2;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"filterCell" forIndexPath:indexPath];
    
    NSString *filtername = [[NSString alloc] initWithFormat:@"%@", _filters[indexPath.row]];
    cell.textLabel.text = filtername;
    return cell;
}


